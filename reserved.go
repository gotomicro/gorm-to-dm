var ReservedWords = hashset.New(funk.Map(ReservedWordsList, func(s string) interface{} { return s }).([]interface{})...)

func IsReservedWord(v string) bool {
	return ReservedWords.Contains(strings.ToUpper(v)) || HasNumberPrefix(v)
}

func HasNumberPrefix(v string) bool {
	for _, n := range NumberList {
		if strings.HasPrefix(v, n) {
			return true
		}
	}
	return false
}

